/**
 * add - example function
 * @a: first number
 * @b: second number
 * Return: operation
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - example function
 * @a: first number
 * @b: second number
 * Return: operation
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - example function
 * @a: first number
 * @b: second number
 * Return: operation
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - example function
 * @a: first number
 * @b: second number
 * Return: operation
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - example function
 * @a: first number
 * @b: second number
 * Return: operation
 */
int mod(int a, int b)
{
	return (a % b);
}
